$(function () {

    //载入省
    for (i = 0; i < region.province.num; i++) {
        $('.province').append('<li _key="' + region.province.data[i][0] + '">' + region.province.data[i][2] + '</li>');
    }

    //加载城市
    var first_province = region.city[region.province.data[0][0]];
    for (j = 0; j < first_province.num; j++) {
        $('.city').append('<li>' + first_province.data[j][1] + '</li>');
    }

    //点击省加载城市
    $('.province li').live('click', function () {
        $('.city').html('');
        $('.province li').css({'border-color': '#efefef', 'background-color': 'white', 'color': 'gray'});
        $(this).css({'border-color': '#005EAC', 'background-color': '#005EAC', 'color': 'white'});
        $('input[name="region_city"]').val('');
        $('input[name="region_province"]').val($(this).html());
        var city_key = $(this).attr('_key');
        for (m = 0; m < region.city[city_key].num; m++) {
            $('.city').append('<li>' + region.city[city_key].data[m][1] + '</li>');
        }
    });

    //点击城市
    $('.city li').live('click', function () {
        $('.city li').css({'border-color': '#efefef', 'background-color': 'white', 'color': 'gray'});
        $(this).css({'border-color': '#005EAC', 'background-color': '#005EAC', 'color': 'white'});
        $('input[name="region_city"]').val($(this).html());
        $('input[name="city"]').val($('input[name="region_province"]').val()+' '+$(this).html());
        $('.popup_dialog').hide();
    });


});